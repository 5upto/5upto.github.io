export default function Contact() {
  const sectionRef = useRef<HTMLElement>(null)
  const headingRef = useRef<HTMLHeadingElement>(null)
  const iconsRef = useRef<HTMLDivElement>(null)

  useEffect(() => {
    const ctx = gsap.context(() => {
      gsap.fromTo(headingRef.current,
        { y: 40, opacity: 0 },
        { y: 0, opacity: 1, duration: 0.8, scrollTrigger: { trigger: sectionRef.current, start: 'top 75%' } }
      )
      const items = iconsRef.current?.querySelectorAll('.social-icon')
      if (items) {
        gsap.fromTo(items,
          { y: 20, opacity: 0, scale: 0.8 },
          { y: 0, opacity: 1, scale: 1, duration: 0.4, stagger: 0.08, ease: 'back.out(1.7)', scrollTrigger: { trigger: sectionRef.current, start: 'top 70%' } }
        )
      }
    }, sectionRef)
    return () => ctx.revert()
  }, [])

  return (
    <section ref={sectionRef} id="contact" className="py-24 px-4 relative">
      <div className="max-w-3xl mx-auto text-center">
        <h2 ref={headingRef} className="section-heading text-center">
          Get In <span className="gradient-text">Touch</span>
        </h2>
        <p className="text-[var(--text-muted)] mb-12 max-w-xl mx-auto">
          Have a question, collaboration idea, or just want to say hi? Feel free to reach out.
        </p>
        <div ref={iconsRef} className="flex justify-center gap-4 flex-wrap">
          {socialLinks.map((link) => (
            <a
              key={link.label}
              href={link.href}
              target="_blank"
              rel="noopener noreferrer"
              title={link.label}
              className={`social-icon w-12 h-12 rounded-full bg-[var(--bg-card)] border border-[var(--border)] flex items-center justify-center text-[var(--text-muted)] ${link.hover} ${link.label === 'Email' ? 'group/gmail' : ''} hover:border-[var(--border-hover)] hover:scale-110 transition-all duration-300`}
            >
              {link.icon}
            </a>
          ))}
        </div>
      </div>

      <div className="mt-16 text-center text-[var(--text-muted)] text-sm">
        <p>Dhaka, Bangladesh</p>
        <p className="mt-2">&copy; {new Date().getFullYear()} Shawon Ghosh.</p>
      </div>
    </section>
  )
}
